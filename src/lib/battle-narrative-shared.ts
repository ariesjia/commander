export function randomInventoryBattleFlairLine(
  itemName: string,
  phase: "win" | "lose",
  random: () => number = Math.random,
): string {
  const name = itemName.trim() || "补给";
  const pool = phase === "win" ? INVENTORY_FLAIR_WIN : INVENTORY_FLAIR_LOSE;
  return randomPick(pool, random)(name);
}

